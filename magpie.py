userinput = int(input("Please enter a number between 1-7: "))

if userinput == 1:
    print("One for sorrow")
elif userinput == 2:
    print("Two for joy")
elif userinput == 3:
    print("Three for a girl")
elif userinput == 4:
    print("Four for a boy")
elif userinput == 5:
    print("Five for silver")
elif userinput == 6:
    print("Six for gold")
elif userinput == 7:
    print("Seven for a secret never to be told")
else:
    print("Not a permitted number.")